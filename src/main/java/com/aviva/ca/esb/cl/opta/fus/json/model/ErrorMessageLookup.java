package com.aviva.ca.esb.cl.opta.fus.json.model;


public class ErrorMessageLookup {
	private String errorCode;
	private String errorDesc;
	private String errorDesc_fr;
	
	
	public String getErrorCode() {
		return errorCode;
	}
	public void setErrorCode(String errorCode) {
		this.errorCode = errorCode;
	}
	public String getErrorDesc() {
		return errorDesc;
	}
	public void setErrorDesc(String errorDesc) {
		this.errorDesc = errorDesc;
	}
	public String getErrorDesc_fr() {
		return errorDesc_fr;
	}
	public void setErrorDesc_fr(String errorDesc_fr) {
		this.errorDesc_fr = errorDesc_fr;
	}
	
}
